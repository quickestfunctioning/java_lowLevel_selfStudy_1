package variable;

public class Var6 {
    public static void main(String[] args) {

        int a=232839; // 엄청 큰 범위 정수, 사실상 메모리값은 매우 저렴해서 int쓰는게 가장 좋음
        long b=11823792387942L; // 가장 큰 범위 정수, 숫자 크면 이것도 쓰긴 함

        double c=10.0; // 엄청난 정밀도, float은 정밀도가 너무 낮아서 무조건 double 씀

        boolean d=true; // 조건문에서 사용

        String e="굿";
    }


}
